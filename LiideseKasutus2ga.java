public class LiideseKasutus2ga implements Liides{
    public double korruta(double a){
        return a * 2;
    }

    public double jaga(double a){
        return a / 2;
    }
    public double liida(double a){
        return a + 2;
    }
    public double lahuta(double a){
        return a - 2;
    }
}